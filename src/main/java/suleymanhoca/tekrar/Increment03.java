package suleymanhoca.tekrar;

public class Increment03 {
    public static void main(String[] args) {
        // int data type'inda iki tane variable olusturun.
        // bu variablelarin en kisa yolu kullanarak 1 arttirin.
        // en kisa yolu sadece 1 arttirmak icin kullanilir.

        int sayi1= 12;
        sayi1++;
        System.out.println(sayi1);

        int sayi2=20;
        sayi2 = sayi2+1;
        sayi2+= 1;
        sayi2++;
        System.out.println(sayi2);
    }
}
